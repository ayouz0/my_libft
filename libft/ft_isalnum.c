#include "libft.h"

int	ft_isalnum(int c)
{
	unsigned char	i;

	i = (unsigned char) c;
	if (((i <= 90 && i >= 65) || (i <= 122 && i >= 97))
		|| (i <= '9' && i >= '0'))
		return (1);
	return (0);
}
